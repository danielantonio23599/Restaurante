    private String verificaCampos() {
        String retorno = "";
        if (jtfNome.getText().equals("")) {
            retorno = "Campo Nome";
        }

        if (jtfValor.getText().equals("")) {
            retorno += ", Valor";
        }
        if (!retorno.equals("")) {
            retorno += " se encontra(ão) 'vazio(s)', preencha-o(s) por gentileza!!";
        }
        return retorno;
    }

    private PagamentoBEAN getDados() {
        PagamentoBEAN des = new PagamentoBEAN();
        des.setNome(jtfNome.getText() + "");
        des.setAcrescimo(Float.parseFloat(jtfValor.getText() + ""));
        return des;
    }

    private void prencheCampos(PagamentoBEAN c) {
        cod = c.getCodigo();
        jtfNome.setText(c.getNome());
        jtfValor.setText(c.getAcrescimo() + "");
    }
